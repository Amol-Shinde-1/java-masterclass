/*
Question:
Write a Java program to declare multiple byte variables and print them.
*/

class Program02_DeclareMultipleBytes {

    public static void main(String[] args) {

        byte a = 10;
        byte b = 20;
        byte c = 30;

        System.out.println("a = " + a);
        System.out.println("b = " + b);
        System.out.println("c = " + c);

    }
}
