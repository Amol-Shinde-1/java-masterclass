/*
Question:
Write a Java program to compare two byte values.
*/

class Program07_ByteComparison {

    public static void main(String[] args) {

        byte x = 25;
        byte y = 40;

        System.out.println("x > y : " + (x > y));
        System.out.println("x < y : " + (x < y));
        System.out.println("x == y : " + (x == y));

    }
}
