/*
Question:
Write a Java program to declare a double variable and print its value.

Concept:
Declaration and Initialization of double.

Explanation:
This program declares a variable of type double and stores a decimal value.
The double data type is used when high precision decimal numbers are required.
Finally, the value is printed using System.out.println().

Time Complexity: O(1)
Space Complexity: O(1)
*/

class DeclareDouble {

    public static void main(String[] args) {

        double salary = 45678.987654;

        System.out.println("Salary = " + salary);
    }
}
