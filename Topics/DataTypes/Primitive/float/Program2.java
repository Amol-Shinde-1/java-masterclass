/*
Question:
Write a Java program to print the minimum and maximum values of the float data type.

Concept:
Float.MIN_VALUE and Float.MAX_VALUE.
*/

class FloatMinMax {

    public static void main(String[] args) {

        System.out.println("Minimum Value = " + Float.MIN_VALUE);
        System.out.println("Maximum Value = " + Float.MAX_VALUE);
    }
}
