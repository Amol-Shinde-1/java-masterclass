/*
Question:
Write a Java program to check whether a number is even or odd.

Concept:
Modulo Operator.
*/

class Program9 {

    public static void main(String[] args) {

        int number = 27;

        if (number % 2 == 0)
            System.out.println("Even Number");
        else
            System.out.println("Odd Number");
    }
}
