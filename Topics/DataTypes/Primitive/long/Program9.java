/*
Question:
Write a Java program to store the population of different countries using the long data type.

Concept:
Real-world application of long.
*/

class PopulationCounter {

    public static void main(String[] args) {

        long india = 1450000000L;
        long usa = 342000000L;
        long japan = 123000000L;

        System.out.println("India Population : " + india);
        System.out.println("USA Population    : " + usa);
        System.out.println("Japan Population  : " + japan);
    }
}
